package com.sinosoft.stringcomparison;

public class StringComparisionException extends RuntimeException {

	/**
	 * 
	 */
	
	private static final long serialVersionUID = -293213844441471932L;

	public StringComparisionException() {
		super();
		
	}

	public StringComparisionException(String message, Throwable cause) {
		super(message, cause);
		
	}

	public StringComparisionException(String message) {
		super(message);
		
	}

	public StringComparisionException(Throwable cause) {
		super(cause);
		
	}
	
	

}
