package com.sinosoft.stringcomparison;

public class MetricsException extends StringComparisionException {

	private static final long serialVersionUID = 2624878902810720896L;

	public MetricsException() {
		super();
	}

	public MetricsException(String message, Throwable cause) {
		super(message, cause);
	}

	public MetricsException(String message) {
		super(message);
	}

	public MetricsException(Throwable cause) {
		super(cause);
	}

}
